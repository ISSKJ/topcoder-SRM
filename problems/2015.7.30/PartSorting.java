    public int[] process(int[] data, int swap) {

        for (int i = 0; i < data.length; i++) {
            int target = i;
            for (int j = i+1; j < data.length; j++) {
                if (data[j] > data[target]) {
                    target = j;
                }
                if (j - i >= swap) {
                    break;
                }
            }
            while (target > i && swap > 0) {
                int tmp = data[target-1];
                data[target-1] = data[target];
                data[target] = tmp;
                swap--;
                target--;
            }
        }
        return data;
    }

